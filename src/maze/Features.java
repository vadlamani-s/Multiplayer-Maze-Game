package maze;

/**
 * The enum IControllerGui class represents all the features present in the maze. All the features
 * that can be represented in the maze are represented as an enum type.
 */
public enum Features {
  /**
   * Thief features.
   */
  THIEF,
  /**
   * Gold features.
   */
  GOLD,
  /**
   * Bat features.
   */
  BAT,
  /**
   * Wampus features.
   */
  WAMPUS,
  /**
   * Pit features.
   */
  PIT,
  /**
   * Hallway features.
   */
  HALLWAY;
}
